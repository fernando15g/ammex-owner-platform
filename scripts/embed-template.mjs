// Re-embeds templates/proposal-template.xlsx into lib/documents/proposalTemplate.js
// Run after changing the template:  node scripts/embed-template.mjs
import { readFileSync, writeFileSync } from "fs";

const raw = readFileSync("templates/proposal-template.xlsx");
const b64 = raw.toString("base64");

writeFileSync("lib/documents/proposalTemplate.js",
`// AUTO-GENERATED by scripts/embed-template.mjs — do not edit by hand.
// The proposal template, embedded as code so it always ships with the
// serverless bundle (file-tracing prunes loose files, and it only fails on
// Vercel — which is the worst place to find out).
//
// TO UPDATE: replace templates/proposal-template.xlsx, then run
//   node scripts/embed-template.mjs

export const PROPOSAL_TEMPLATE_B64 = "${b64}";

export function proposalTemplateBuffer() {
  return Buffer.from(PROPOSAL_TEMPLATE_B64, "base64");
}
`);
console.log(`embedded ${raw.length.toLocaleString()} bytes`);
